# What is a List in Python?
# A list in Python is a collection of items that are ordered, mutable (changeable),
#  and allow duplicate values. Lists can store elements of different data types,
#  such as integers, strings, and even other lists.


my_list = [1,2,3,4,5,6]

# reverse list
my_list.reverse()
print(my_list)

# Modifying Lists
fruits = ['banana','mongo','kiwi'];
fruits[0] = 'graps'
print(fruits[0])

# append 
my_list.append(10)
print(my_list)

# insert

my_list.insert(1,20)
print(my_list)