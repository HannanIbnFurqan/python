# What is a Tuple in Python?
# A tuple in Python is an ordered, immutable collection of elements. Tuples are similar to lists but cannot be modified (immutable) once created.

# Key Features of Tuples:
# Ordered → Elements are stored in a specific sequence.
# Immutable → Cannot be changed (no addition, removal, or modification).
# Allow Duplicates → Can have repeated values.
# Can Hold Different Data Types → Strings, numbers, booleans, lists, etc.

my_tuple = (1,2,3,4,5)